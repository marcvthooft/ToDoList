@implementation lolToDoListViewController

- (void) loadInitialData {
    lolToDoItem *item1 = [[lolToDoItem alloc] init];
    item1.itemName = @"eat";
    [self.todoItems addObject:item1];
    lolToDoItem *item2 = [[lolToDoItem alloc] init];
    item2.itemName = @"sleep";
    [self.todoItems addObject:item2];
    lolToDoItem *item3 = [[lolToDoItem alloc] init];
    item3.itemName = @"rave";
    [self.todoItems addObject:item3];
    lolToDoItem *item4 = [[lolToDoItem alloc] init];
    item4.itemName = @"repeat";
    [self.todoItems addObject:item4];
}

- (IBAction)unwindToList:(UIStoryboardSegue *)segue
{
    lolAddTodoItemViewController *source = [segue sourceViewController];
    lolToDoItem *item = source.toDoItem;
    if (item != nil) {
        [self.todoItems addObject:item];
        [self.tableView reloadData];
    }
}

